#!/usr/bin/env bash
# Results Collector — aggregates JSONL results into summary report
set -euo pipefail

_log() { printf '[results] %s  %s\n' "$(date +%H:%M:%S)" "$*" >&2; }

# BENCH-SEC-03: Validate file path — reject shell metacharacters and traversal
_validate_path() {
    local path="$1" label="${2:-path}"
    if [[ -z "$path" ]]; then
        _log "ERROR: $label is empty"; return 1
    fi
    # Reject shell metacharacters, backticks, $(), and null bytes
    if printf '%s' "$path" | grep -qE '[;|&`$><]|\$\(|\\0'; then
        _log "ERROR: $label contains unsafe characters: '$path'"; return 1
    fi
    # Reject directory traversal
    if printf '%s' "$path" | grep -qE '(^|/)\.\.(/|$)'; then
        _log "ERROR: $label contains directory traversal: '$path'"; return 1
    fi
    return 0
}

# Aggregate results from a JSONL file into a summary JSON
aggregate_jsonl() {
    local results_file="$1"
    _validate_path "$results_file" "results_file" || return 1
    [[ -f "$results_file" ]] || { _log "no results file: $results_file"; return 1; }

    # BENCH-SEC-03: Pass file path via env var instead of interpolating into Python code
    BENCH_RESULTS_FILE="$results_file" BENCH_TIMESTAMP="$(date -u +%Y-%m-%dT%H:%M:%SZ)" \
    python3 -c '
import json, sys, os

results_file = os.environ["BENCH_RESULTS_FILE"]
timestamp = os.environ["BENCH_TIMESTAMP"]

results = []
with open(results_file) as f:
    for line in f:
        line = line.strip()
        if line:
            results.append(json.loads(line))

if not results:
    print(json.dumps({"error": "no results"}))
    sys.exit(0)

total = len(results)
resolved = sum(1 for r in results if r.get("resolved", False))
failed = sum(1 for r in results if r.get("status") in ("fail", "no_changes"))
errors = total - resolved - failed
avg_time = sum(r.get("wall_time_seconds", 0) for r in results) / total
avg_cycles = sum(r.get("cycles", 1) for r in results) / total
rogue_total = sum(r.get("rogue_writes", 0) for r in results)
rogue_instances = sum(1 for r in results if r.get("rogue_writes", 0) > 0)

summary = {
    "total": total,
    "resolved": resolved,
    "failed": failed,
    "errors": errors,
    "resolve_rate": round(resolved / total * 100, 1),
    "avg_wall_time_seconds": round(avg_time, 1),
    "avg_cycles": round(avg_cycles, 1),
    "rogue_write_instances": rogue_instances,
    "rogue_write_total": rogue_total,
    "rogue_write_rate": round(rogue_instances / total * 100, 1),
    "model": results[0].get("model", "unknown"),
    "timestamp": timestamp
}
print(json.dumps(summary, indent=2))
'
}

# Generate markdown report from summary JSON + results JSONL
generate_report() {
    local summary_file="$1"
    local results_file="$2"
    local suite="${3:-custom}"

    _validate_path "$summary_file" "summary_file" || return 1
    _validate_path "$results_file" "results_file" || return 1
    [[ -f "$summary_file" ]] || { _log "no summary: $summary_file"; return 1; }

    local total resolved failed errors rate avg_time avg_cycles rogue_rate ts model
    total="$(jq -r '.total' "$summary_file")"
    resolved="$(jq -r '.resolved' "$summary_file")"
    failed="$(jq -r '.failed' "$summary_file")"
    errors="$(jq -r '.errors' "$summary_file")"
    rate="$(jq -r '.resolve_rate' "$summary_file")"
    avg_time="$(jq -r '.avg_wall_time_seconds' "$summary_file")"
    avg_cycles="$(jq -r '.avg_cycles' "$summary_file")"
    rogue_rate="$(jq -r '.rogue_write_rate' "$summary_file")"
    ts="$(jq -r '.timestamp' "$summary_file")"
    model="$(jq -r '.model' "$summary_file")"

    cat <<REPORT
# Benchmark Report — $suite — $(date +%Y-%m-%d)

| Metric | Value |
|--------|-------|
| Suite | $suite |
| Model | $model |
| Instances run | $total |
| Resolved | $resolved |
| Failed | $failed |
| Errors | $errors |
| Resolve rate | ${rate}% |
| Avg cycles | $avg_cycles |
| Avg wall time | ${avg_time}s |
| Rogue write rate | ${rogue_rate}% |
| Timestamp | $ts |

## Per-Instance Results

| Instance | Status | Time(s) | Rogue | Match |
|----------|--------|---------|-------|-------|
REPORT

    if [[ -f "$results_file" ]]; then
        while IFS= read -r line; do
            [[ -z "$line" ]] && continue
            local rid rstatus rdur rrogue rmatch
            rid="$(printf '%s' "$line" | jq -r '.instance_id')"
            rstatus="$(printf '%s' "$line" | jq -r '.status')"
            rdur="$(printf '%s' "$line" | jq -r '.wall_time_seconds')"
            rrogue="$(printf '%s' "$line" | jq -r '.rogue_writes')"
            rmatch="$(printf '%s' "$line" | jq -r '.patch_match')"
            printf '| %s | %s | %s | %s | %s |\n' "$rid" "$rstatus" "$rdur" "$rrogue" "$rmatch"
        done < "$results_file"
    fi

    printf '\n---\n*Generated by OrchyStraw benchmark harness*\n'
}

if [[ "${BASH_SOURCE[0]}" == "$0" ]]; then
    case "${1:-}" in
        --aggregate) aggregate_jsonl "$2" ;;
        --report)    generate_report "$2" "$3" "${4:-custom}" ;;
        *)           echo "Usage: results-collector.sh --aggregate <results.jsonl> | --report <summary.json> <results.jsonl> [suite]" >&2 ;;
    esac
fi
